public class Dice {
    
    private int dice;

    /**
     * Constructor
     *
     * @param dice the value of dice
     */
    public Dice(int dice) {
        this.dice = dice;
    }

    /**
     * Get the value of dice
     *
     * @return the value of dice
     */
    public int getDice() {
        return dice;
    }

    /**
     * Set the value of dice
     *
     * @param dice new value of dice
     */
    public void setDice(int dice) {
        this.dice = dice;
    }
}
